<h3>Gallery Wrap</h3>
<p>
  A separate technique for finishing canvas prints. Gallery wrap is a method of stretching an artist's canvas so that the canvas wraps around the sides. The sides of the canvas are prepared and primed in the same manner as the face and tend to continue the image appearing on the face.  the term gallery wrap refers to an image that appears on the sides of the frame as well as the front. 
</p>
